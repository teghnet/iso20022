type schema struct {
	Pkg      string
	TargetNS string
	Folder   string
}

func Generate(args []string) error {
	var i, o string
	if err := x.FlagsParse(args,
		x.Flag(&i, "i", "", "input dir holding all XSD files"),
		x.Flag(&o, "o", "", "Go package output dir"),
	); err != nil {
		return err
	}
	if i == "" || o == "" {
		return fmt.Errorf("missing required flags: -i and -o")
	}
	files, err := filepath.Glob(fmt.Sprintf("%s/*.xsd", i))
	if err != nil {
		return err
	}
	var schemas []schema
	set := make(map[string]bool)
	for _, f := range files {
		log.Printf("finished: %v", f)
		pkg, folder, err := generateFile(o, f)
		if err != nil {
			log.Printf("failed to generate go package for '%s': %s\n", f, err)
			continue
		}
		targetNS, err := getTargetNS(f)
		if err != nil {
			log.Printf("failed to get target namespace for '%s': %s\n", f, err)
			return err
		}
		if set[targetNS] {
			continue
		}
		set[targetNS] = true
		schemas = append(schemas, schema{
			Pkg:      pkg,
			TargetNS: targetNS,
			Folder:   folder,
		})
		log.Printf(" - added: %s\n", targetNS)
	}
	return renderISO20022Package(o, schemas)
}

func generateFile(dir, f string) (string, string, error) {
	_, fil := path.Split(f)

	// https://sonra.io/eight-things-need-know-iso-20022-xml-messages/
	parts := strings.Split(fil, ".")
	businessArea := parts[0]
	messageID := parts[1]
	variant := parts[2]
	version := parts[3]

	folder := path.Join(dir, "document", businessArea, messageID, variant, version)
	if err := os.MkdirAll(folder, 0777); err != nil {
		return "", "", fmt.Errorf("failed to create directory '%s': %s", folder, err)
	}

	messageDefinitionID := fmt.Sprintf("%s_%s_%s_%s", businessArea, messageID, variant, version)
	var cfg xsdgen.Config
	cfg.Option(
		xsdgen.IgnoreAttributes("id", "href", "offset"),
		xsdgen.IgnoreElements("comment"),
		xsdgen.PackageName(messageDefinitionID),
		xsdgen.Replace("_", ""),
		xsdgen.HandleSOAPArrayType(),
		xsdgen.SOAPArrayAsSlice(),
	)
	if err := cfg.GenCLI("-o", path.Join(folder, fmt.Sprintf("%s.gen.go", messageDefinitionID)), f); err != nil {
		return "", "", err
	}
	return messageDefinitionID, folder, nil
}

func getTargetNS(f string) (string, error) {
	data, err := os.ReadFile(f)
	if err != nil {
		return "", fmt.Errorf("there was an error reading the file: %v", err)
	}
	schemas, err := xsd.Parse(data)
	return schemas[0].TargetNS, err
}

func renderISO20022Package(dir string, schemas []schema) error {
	templateData := struct{ Schemas []schema }{Schemas: schemas}

	var buf bytes.Buffer
	err := template.Must(template.New("").Parse(templateText)).Execute(&buf, templateData)
	if err != nil {
		return fmt.Errorf("failed to render iso20022 package code: %s", err)
	}

	f, err := os.Create(path.Join(dir, "iso20022.gen.go"))
	if err != nil {
		return fmt.Errorf("failed to write iso20022 package code: %s", err)
	}
	defer f.Close()

	p, err := format.Source(buf.Bytes())
	if err != nil {
		_, err = f.Write(buf.Bytes())
		if err != nil {
			return err
		}
		return fmt.Errorf("failed to format iso20022 package code: %s", err)
	}

	_, err = f.Write(p)
	return err
}

